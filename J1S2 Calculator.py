''' 2) Session 2 Prompt: Write a Python program (.py file) that
prints the sum of two floating point numbers,
the difference between two integers,
and the product of a floating point number and an integer.
In each case, have the program print out the data type of the resulting answer.
'''

myInt1 = 5
myInt2 = 10
myFloat1 = 6.7
myFloat2 = 0.3

print("Sum:", myFloat1 + myFloat2)
print("Data type:", type(myFloat1 + myFloat2))

print("Difference:", myInt1 + myInt2)
print("Data type:", type(myInt1 + myInt2))

print("Product:", myFloat1 + myInt1)
print("Data type:", type(myFloat1 + myInt1))

